class NullArrayException extends Exception {

    public NullArrayException(String message) {
        super(message);
    }

    @Override
    public String getMessage() {
        return super.getMessage(); 
    }

    @Override
    public void printStackTrace() {
        super.printStackTrace(); 
    }
    
    
}
